package com.productsystem.model;

public class Fileway
{
  private int id;
  private String filename;
  private String filename_new;
  private String ftpway;
  private String yxbz;

  public int getId()
  {
    return this.id;
  }

  public void setId(int id)
  {
    this.id = id;
  }

  public String getFilename()
  {
    return this.filename;
  }

  public void setFilename(String filename)
  {
    this.filename = filename;
  }

  public String getFilename_new()
  {
    return this.filename_new;
  }

  public void setFilename_new(String filename_new)
  {
    this.filename_new = filename_new;
  }

  public String getFtpway()
  {
    return this.ftpway;
  }

  public void setFtpway(String ftpway)
  {
    this.ftpway = ftpway;
  }

  public String getYxbz()
  {
    return this.yxbz;
  }

  public void setYxbz(String yxbz)
  {
    this.yxbz = yxbz;
  }
}